
namespace Pfz.DataTypes.Ranges
{
	/// <summary>
	/// Enumeration used by BasicComparer.
	/// </summary>
	public enum BasicComparison
	{
		/// <summary>
		/// &gt;
		/// </summary>
		GreaterThan,

		/// <summary>
		/// &gt;=
		/// </summary>
		GreaterThanOrEqualTo,

		/// <summary>
		/// &lt;
		/// </summary>
		LessThan,

		/// <summary>
		/// &lt;=
		/// </summary>
		LessThanOrEqualTo
	}
}
